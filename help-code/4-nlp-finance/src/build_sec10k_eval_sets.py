def label_sentence(sentence: str) -> str:
    tokens = set(word_tokenize(sentence))
    scores = [(label, len(tokens & keywords)) for label, keywords in LABEL_RULES]
    label, score = max(scores, key=lambda item: item[1])
    return label if score else "other_disclosure"


def clean_for_dataset(sentence: str) -> str:
    replacements = {
        "\uFFFD": "",
        "\u00AE": "",
        "\u2122": "",
        "\u2019": "'",
        "\u2018": "'",
        "\u201C": '"',
        "\u201D": '"',
        "\u00A0": " ",
    }
    for old, new in replacements.items():
        sentence = sentence.replace(old, new)
    sentence = re.sub(r"\s+", " ", sentence)
    return sentence.strip()


def split_for_index(i: int) -> str:
    if i % 10 in {0, 1}:
        return "test"
    if i % 10 == 2:
        return "validation"
    return "train"


def is_disclosure_sentence(sentence: str) -> bool:
    """Filter SEC table-of-contents and exhibit fragments out of teaching sets."""
    if re.search(r"\bPart\s+[IVX]+\b|\bItem\s+\d+[A-Z]?\b", sentence):
        return False
    if re.search(r"\bTable of Contents\b|SIGNATURES|EXHIBIT|FORM 10-K", sentence, flags=re.I):
        return False
    if len(word_tokenize(sentence)) < 8:
        return False
    finance_terms = {
        "sales", "revenue", "services", "products", "risk", "risks", "cash",
        "margin", "cost", "costs", "supply", "regulatory", "capital",
        "expense", "expenses", "iphone", "mac", "ipad", "foreign", "exchange",
    }
    return bool(set(word_tokenize(sentence)) & finance_terms)


def write_jsonl(path: Path, rows):
    with path.open("w", encoding="utf-8") as f:
        for row in rows:
            f.write(json.dumps(row, ensure_ascii=False) + "\n")


def bio_tags(sentence: str):
    patterns = [
        ("ORG", r"\bApple\b|\bCompany\b"),
        ("MONEY", r"\$[0-9,]+(?:\s+million)?"),
        ("DATE", r"\b2025\b|\b2024\b|\b2023\b"),
        ("GEO", r"\bU\.S\.\b|\bChina\b"),
        ("PRODUCT", r"\bServices\b|\bAppleCare\b|\bProducts\b"),
        ("FIN_METRIC", r"\bnet sales\b|\brevenue\b|\bshare repurchases\b|\bdividends\b"),
    ]
    tokens = re.findall(r"\$[0-9,]+|[A-Za-z]+(?:\.[A-Za-z]+)*\.?|[0-9]+|[,.;]", sentence)
    tags = ["O"] * len(tokens)
    lowered = " ".join(tokens).lower()
    for label, pattern in patterns:
        for match in re.finditer(pattern, sentence, flags=re.I):
            phrase_tokens = re.findall(r"\$[0-9,]+|[A-Za-z]+(?:\.[A-Za-z]+)*\.?|[0-9]+|[,.;]", match.group(0))
            for start in range(len(tokens) - len(phrase_tokens) + 1):
                window = " ".join(tokens[start : start + len(phrase_tokens)]).lower()
                if window == " ".join(phrase_tokens).lower() and all(t == "O" for t in tags[start : start + len(phrase_tokens)]):
                    tags[start] = f"B-{label}"
                    for offset in range(1, len(phrase_tokens)):
                        tags[start + offset] = f"I-{label}"
                    break
    return list(zip(tokens, tags))


def main():
    EVAL_DIR.mkdir(parents=True, exist_ok=True)
    text = load_apple_10k()
    sentences = [
        clean_for_dataset(s) for s in sent_tokenize(text)
        if 50 <= len(s) <= 260 and is_disclosure_sentence(s)
    ]

    sentence_pool = [
        {
            "id": f"sent_{i:04d}",
            "sentence": s,
            "tokens": word_tokenize(s, lower=False),
            "source": "Apple FY2025 Form 10-K excerpt",
        }
        for i, s in enumerate(sentences[:250], start=1)
    ]
    write_jsonl(EVAL_DIR / "apple_2025_sentence_pool.jsonl", sentence_pool)

    rows = []
    for i, s in enumerate(sentences[:160], start=1):
        rows.append({
            "id": f"cls_{i:04d}",
            "sentence": s,
            "label": label_sentence(s),
            "split": split_for_index(i),
            "label_source": "keyword_heuristic_for_teaching",
        })
    with (EVAL_DIR / "apple_2025_sentence_classification.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0]))
        writer.writeheader()
        writer.writerows(rows)

    write_jsonl(EVAL_DIR / "apple_2025_qa.jsonl", CURATED_QA)
    write_jsonl(EVAL_DIR / "apple_2025_summarization.jsonl", SUMMARY_RECORDS)

    with (EVAL_DIR / "apple_2025_keywords_gold.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["keyword", "rationale"])
        writer.writerows(KEYWORDS)

    with (EVAL_DIR / "apple_2025_translation_pairs.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["en", "de"])
        writer.writerows(TRANSLATION_PAIRS)

    with (EVAL_DIR / "apple_2025_ner_tokens.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["sentence_id", "token", "bio_tag"])
        for idx, sentence in enumerate(NER_SENTENCES, start=1):
            for token, tag in bio_tags(sentence):
                writer.writerow([f"ner_{idx:03d}", token, tag])
            writer.writerow([f"ner_{idx:03d}", "", ""])

    lm_sentences = sentences[:120]
    (EVAL_DIR / "apple_2025_lm_corpus.txt").write_text("\n".join(lm_sentences), encoding="utf-8")

    token_counts = Counter(t.lower() for s in lm_sentences for t in word_tokenize(s))
    with (EVAL_DIR / "apple_2025_vocabulary.csv").open("w", newline="", encoding="utf-8") as f:
        writer = csv.writer(f)
        writer.writerow(["token", "count"])
        writer.writerows(token_counts.most_common(500))

    (EVAL_DIR / "README.md").write_text(
        """# Apple 2025 SEC 10-K Evaluation Sets

These small datasets support the `4-nlp-finance` notebooks with concrete SEC filing tasks.
They are intended for teaching and lightweight evaluation, not for benchmark claims.

## Files

- `apple_2025_sentence_pool.jsonl`: reusable Apple 10-K sentence pool with tokenized text.
- `apple_2025_sentence_classification.csv`: weakly labeled train/validation/test sentence classification set.
- `apple_2025_qa.jsonl`: curated extractive QA examples over Apple 2025 filing facts.
- `apple_2025_summarization.jsonl`: short source/summary pairs for disclosure summarization.
- `apple_2025_keywords_gold.csv`: gold-style keyword list with rationales.
- `apple_2025_ner_tokens.csv`: BIO token labels for ORG, MONEY, DATE, GEO, PRODUCT, and FIN_METRIC.
- `apple_2025_translation_pairs.csv`: short English-to-German financial disclosure translation pairs.
- `apple_2025_lm_corpus.txt`: sentence-per-line corpus for n-gram/RNN/GPT-style language modeling examples.
- `apple_2025_vocabulary.csv`: token frequency vocabulary from the LM corpus.

## Source

Apple Inc. fiscal 2025 Form 10-K, filed October 31, 2025, for fiscal year ended September 27, 2025.
""",
        encoding="utf-8",
    )

    print(f"Wrote evaluation sets to {EVAL_DIR}")


if __name__ == "__main__":
    main()
